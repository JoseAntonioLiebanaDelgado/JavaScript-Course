// 24. Operaciones en JavaScript

const num1 = 30;
const num2 = 20;

let resultado;

// Suma
resultado = num1 + num2;

// Resta
resultado = num1 - num2;

// Multiplicación
resultado = num1 * num2;

// División
resultado = num1 / num2;

// Módulo (Resto de la división)
resultado = num1 % num2;

// Exponencial
resultado = num1 ** num2; // num1 elevado a la potencia de num2

// Incremento
resultado++;

// Decremento
resultado--;

// Incremento con asignación
resultado += 5; // resultado = resultado + 5;

// Decremento con asignación
resultado -= 5; // resultado = resultado - 5;

console.log(resultado);