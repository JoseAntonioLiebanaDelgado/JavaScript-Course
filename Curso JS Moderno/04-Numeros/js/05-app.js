// 27. Incrementos o Decrementos

let puntaje = 10;

// Incremento
puntaje++; // Incrementa en 1
++ puntaje; // Incrementa en 1
puntaje += 5; // Incrementa en 5
console.log(puntaje); // 16

// Decremento
puntaje--; // Decrementa en 1
--puntaje; // Decrementa en 1
puntaje -= 3; // Decrementa en 3
console.log(puntaje); // 12

