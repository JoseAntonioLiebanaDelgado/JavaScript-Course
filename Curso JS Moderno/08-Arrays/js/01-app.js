// 48. Crear Arrays en JavaScript

// Los arreglo sirven para agrupar elementos
// Se pueden crear de diferentes maneras
// La primera posición de un array es el índice 0

const numeros = [10, 20, 30]; // Array de números
const meses = new Array('Enero', 'Febrero', 'Marzo'); // Array de strings

// Tambien se pueden crear arrays con diferentes tipos de datos
const datos = ["Hola", 10, true, "si", null, {nombre: "Jose", trabajo: "Desarrollador"}]; // Array con diferentes tipos de datos

console.log(numeros);
console.log(meses);
console.log(datos);
