// 51. Agregar nuevos valores a un array

// Aun siendo const, los valores de un array se pueden modificar
const meses = ['Enero', 'Febrero', 'Marzo', 'Abril', 'Mayo', 'Junio', 'Julio'];

// Agregar un nuevo valor al inicio del Array
meses[0] = 'Nuevo mes'; // Modificar un valor existente
// Agregar un nuevo valor al final del Array
meses[10] = 'Ultimo mes';