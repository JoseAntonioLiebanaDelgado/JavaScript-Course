// 29. Operador Mayor que y Menor Que

const numero1 = 20;
const numero2 = "20";
const numero3 = 30;

// Operador Mayor que
console.log(numero1 > numero3); // false, porque 20 no es mayor que 30
console.log(numero3 > numero1); // true, porque 30 es mayor que 20

// Operador Menor que
console.log(numero1 < numero3); // true, porque 20 es menor que 30
console.log(numero3 < numero1); // false, porque 30 no es menor que 20
