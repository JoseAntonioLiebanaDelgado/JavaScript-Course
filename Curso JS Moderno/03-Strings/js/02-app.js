// 17. String Methods - includes y length

const producto = "Monitor 20 pulgadas";
console.log(producto);

// Los Strings en JavaScript tienen varios métodos útiles para manipularlos
console.log(producto.length); // Longitud del string
console.log(producto.includes("Monitor")); // Verifica si el string contiene "Monitor"
console.log(producto.includes("Tablet")); // Verifica si el string contiene "Tablet"
